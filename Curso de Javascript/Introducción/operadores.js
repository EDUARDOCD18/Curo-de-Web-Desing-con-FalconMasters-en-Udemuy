/* Operador de suma */
const suma = 10 + 10;
console.log(suma);

/* Operador de resta */
const resta = 10 - 3;
console.log(resta);

/* Operador de módulo, el restante de la división */
const modulo = 10 % 3;
console.log(modulo);

